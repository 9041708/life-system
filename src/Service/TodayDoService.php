    private static function getPlaceFromAmap(string $city, ?int $isFree = null): array
    {
        $amapKey = Config::get('amap_key', '');
        if (empty($amapKey)) return [];

        $cacheKey = "amap_{$city}_" . ($isFree ?? 'all');
        $cacheDir = sys_get_temp_dir() . '/today_do_cache';
        if (!is_dir($cacheDir)) @mkdir($cacheDir, 0755, true);
        $cacheFile = $cacheDir . '/' . md5($cacheKey) . '.json';

        if (file_exists($cacheFile) && time() - filemtime($cacheFile) < 3600 * 6) {
            $cached = @json_decode(file_get_contents($cacheFile), true);
            if (is_array($cached)) return $cached;
        }

        $types = '110200|110201|110202|110203|110204|110205|110206|110207|110208|110209|110210|110300';
        $results = [];
        $page = 1;
        $maxPages = 3;

        while ($page <= $maxPages) {
            $url = 'https://restapi.amap.com/v3/place/text?' . http_build_query([
                'key' => $amapKey,
                'keywords' => '景点|公园|博物馆|古镇|寺庙|广场|步行街|游乐场|动物园|植物园|水族馆|滑雪场|漂流|温泉|度假村',
                'city' => $city,
                'types' => $types,
                'offset' => 25,
                'page' => $page,
                'output' => 'json',
                'extensions' => 'all',
            ]);

            $ch = curl_init();
            curl_setopt_array($ch, [
                CURLOPT_URL => $url,
                CURLOPT_RETURNTRANSFER => true,
                CURLOPT_TIMEOUT => 10,
                CURLOPT_SSL_VERIFYPEER => false,
            ]);
            $body = curl_exec($ch);
            curl_close($ch);

            if (!$body) break;
            $data = json_decode($body, true);
            if (empty($data['pois'])) break;

            foreach ($data['pois'] as $poi) {
                $name = $poi['name'] ?? '';
                if (empty($name)) continue;

                $addr = $poi['address'] ?? '';
                $tel = $poi['tel'] ?? '';
                $type = $poi['type'] ?? '';
                $cost = $poi['cost'] ?? '';
                $rating = $poi['biz_ext']['rating'] ?? '';
                $desc = trim(($type ? "类型: {$type}" : '') . ($tel ? " 电话: {$tel}" : ''));

                $poiIsFree = 1;
                $ticketPrice = 0;
                if ($cost && $cost !== '面议') {
                    $costNum = (float)preg_replace('/[^\d.]/', '', $cost);
                    if ($costNum > 0) {
                        $poiIsFree = 0;
                        $ticketPrice = $costNum;
                    }
                }

                if ($isFree !== null && $poiIsFree !== $isFree) continue;

                $results[] = [
                    'name' => $name,
                    'city' => $city,
                    'province' => $poi['pname'] ?? '',
                    'is_free' => $poiIsFree,
                    'ticket_price' => $ticketPrice,
                    'description' => $addr ?: $type,
                    'tips' => $desc,
                    'category' => self::guessCategory($type),
                ];
            }

            $count = (int)($data['count'] ?? 0);
            if ($page * 25 >= $count) break;
            $page++;
        }

        if (!empty($results)) {
            @file_put_contents($cacheFile, json_encode($results, JSON_UNESCAPED_UNICODE));
        }

        return $results;
    }

    private static function guessCategory(string $type): string
    {
        if (stripos($type, '公园') !== false) return '公园';
        if (stripos($type, '博物馆') !== false) return '博物馆';
        if (stripos($type, '寺庙') !== false || stripos($type, '教堂') !== false) return '宗教';
        if (stripos($type, '广场') !== false) return '广场';
        if (stripos($type, '步行街') !== false) return '商业街';
        if (stripos($type, '动物园') !== false || stripos($type, '水族') !== false) return '动物园';
        if (stripos($type, '植物') !== false) return '植物园';
        if (stripos($type, '滑雪') !== false || stripos($type, '漂流') !== false || stripos($type, '温泉') !== false) return '户外';
        return '景点';
    }
