    public static function getRandomFood(string $category = ''): ?array
    {
        $pdo = Database::getConnection();
        $sql = "SELECT * FROM today_food";
        $params = [];
        if ($category !== '') { $sql .= " WHERE category = ?"; $params[] = $category; }
        $sql .= " ORDER BY RAND() LIMIT 1";
        $stmt = $pdo->prepare($sql);
        $stmt->execute($params);
        return $stmt->fetch(\PDO::FETCH_ASSOC) ?: null;
    }

    public static function getRandomPlace(string $city, ?int $isFree = null): ?array
    {
        $pdo = Database::getConnection();
        $where = ["city = ?"];
        $params = [$city];
        if ($isFree !== null) { $where[] = "is_free = ?"; $params[] = $isFree; }
        $sql = "SELECT * FROM today_places WHERE " . implode(' AND ', $where) . " ORDER BY RAND() LIMIT 1";
        $stmt = $pdo->prepare($sql);
        $stmt->execute($params);
        return $stmt->fetch(\PDO::FETCH_ASSOC) ?: null;
    }

